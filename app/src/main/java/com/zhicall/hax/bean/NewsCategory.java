public class NewsCategory {
  String description;
  int id;
  String keywords;
  String name;
  int seq;
  String title;

  public String getDescription() {
    return description;
  }

  public String getKeywords() {
    return keywords;
  }

  public int getId() {
    return id;
  }

  public int getSeq() {
    return seq;
  }

  public String getName() {
    return name;
  }

  public String getTitle() {
    return title;
  }
}